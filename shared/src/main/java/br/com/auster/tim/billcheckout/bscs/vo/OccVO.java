package br.com.auster.tim.billcheckout.bscs.vo;

public class OccVO {

	private String description;
	
	private String value;
	
	private String chargedQty;
	
	private String notChargedQty;

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getChargedQty() {
		return chargedQty;
	}

	public void setChargedQty(String chargedQty) {
		this.chargedQty = chargedQty;
	}

	public String getNotChargedQty() {
		return notChargedQty;
	}

	public void setNotChargedQty(String notChargedQty) {
		this.notChargedQty = notChargedQty;
	}
	
}
